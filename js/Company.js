function Company(
    id,
    name,
    CIF,
    email,
    socialnetworks,
    logo,
    descripcion,
    workersNumber,
    phone,
    address,
    socialnetworks
) {
    this.id = id;
    this.socialnetworks = socialnetworks;
    this.address = address;
    this.name = name;
    this.CIF = CIF;
    this.email = email;
    this.socialnetworks = socialnetworks;
    this.logo = logo;
    this.descripcion = descripcion;
    this.workersNumber = workersNumber;
    this.phone = phone;
}